public class NumberPattern6 {
    public static void main(String[] args) {
        for(int i=4;i>=1;i--){
           // int k=4;
            for(int j=4;j>=i;j--){
                System.out.print(j + " ");
               // k = k-1;
            }
            System.out.println();
        }
    }
}
